public class switchws {
    
    public static void main(String[] args) {
        
    int expression = 8;

    switch(expression){
        case 1: System.out.println("Number is one");
        case 2: System.out.println("Number is two"); break;
        case 3: System.out.println("Number is three"); break;
        case 4: System.out.println("Number is four"); break;

        default: System.out.println("invalid number please try again"); break;

    }
        

    }

}
